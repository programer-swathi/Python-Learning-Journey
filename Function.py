#exponential
def addTwo(x):
    return (x + 2)**2
newvalue=addTwo(10)
print(newvalue)

def subtractTwo(y):
    return y - 2
def multiplyTwo(z):
    return z * 2
def divideTwo(num):
    return num / 2

newvalue1=subtractTwo(10)
newvalue2=multiplyTwo(10)
newvalue3=divideTwo(10)
print(newvalue1)
print(newvalue2)
print(newvalue3)

